    private void simpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_simpanActionPerformed
        if(lbl_kode.getText().equals("")|karyawan.getSelectedIndex()==0|gaji.getText().equals("")|tunjangan.getText().equals("")|lembur.getText().equals("")|bonus.getText().equals("")|potongan.getText().equals("")){
            JOptionPane.showMessageDialog(null, "Isi data terlebih dahulu !");
        }else{
            try{
                st = cn.createStatement();
                st.executeUpdate("INSERT INTO tb_gaji VALUES('"+lbl_kode.getText()+"','"+karyawan.getSelectedItem()+"','"+
                    gaji.getText()+"','"+tunjangan.getText()+"','"+lembur.getText()+"','"+bonus.getText()+"','"+
                    potongan.getText()+"') ");
                tampildata();
                reset();
                lbl_kode.setText("KR000");
                JOptionPane.showMessageDialog(null, "Data berhasil di input !");
            }catch(Exception a){
                JOptionPane.showMessageDialog(null, a.getMessage());
            }
        }
    }//GEN-LAST:event_simpanActionPerformed

