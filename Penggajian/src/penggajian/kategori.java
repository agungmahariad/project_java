    public void tampildata(){
        try{
            st = cn.createStatement();
            tabModel.getDataVector().removeAllElements();
            tabModel.fireTableDataChanged();
            rs = st.executeQuery("SELECT * FROM q_gaji");
            while(rs.next()){
                Object[] data = {
                  rs.getString("kd_karyawan"),
                  rs.getString("nama_karyawan"),
                  rs.getString("jabatan"),
                  rs.getString("gaji_pokok"),
                  rs.getString("tunjangan"),
                  rs.getString("lembur"),
                  rs.getString("bonus"),
                  rs.getString("potongan")
                };
                tabModel.addRow(data);
            }
        }catch(Exception a){
            JOptionPane.showMessageDialog(null, a.getMessage());
        }
    }
    public void tampil_karyawan(){
        try{
            st = cn.createStatement();
            rs1 = st.executeQuery("SELECT * FROM tb_karyawan");
            while(rs1.next()){
                karyawan.addItem(rs1.getString("nama_karyawan"));
            }
        }catch(Exception a) {
            JOptionPane.showMessageDialog(null, a.getMessage());
        }  
    }
    public void setkode(String nama_karyawan){
        try{
            st = cn.createStatement();
            rs2 = st.executeQuery("SELECT * FROM tb_karyawan WHERE nama_karyawan = '"+nama_karyawan+"' ");
            while(rs2.next()){
                kd_karyawan = rs2.getString("kd_karyawan");
            }
        }catch(Exception a){
            JOptionPane.showMessageDialog(null, a.getMessage());
        }
    }
    public String getkode(){
        return kd_karyawan;
    }
